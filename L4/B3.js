
function dienTichHCN(a, b) {
    return a * b;
}


function dienTichHinhVuong(a) {
    return a * a;
}

function dienTichHinhTron(r) {
    const PI = 3.14;
    return PI * r * r;
}

function dienTichTamGiac(a, h) {
    return (a * h) / 2;
}

console.log(dienTichHCN(5, 3));      
console.log(dienTichHinhVuong(4));   
console.log(dienTichHinhTron(3));    
console.log(dienTichTamGiac(6, 4)); 
